function WeekDays(string1){

    if (string1 === "Monday"){
        return 1;
    }
    else if (string1 === "Tuesday"){
        return 2;
    }
    else if (string1 === "Wednesday"){
        return 3;
    }
    else if (string1 === "Thursday"){
        return 4;
    }
    else if (string1 === "Friday"){
        return 5;
    }
    else if (string1 === "Saturday"){
        return 6;
    }
    else if (string1 === "Sunday"){
        return 7;
    }
    else{
        return "error";
    }


}
